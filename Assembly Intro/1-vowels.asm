.MODEL SMALL

.DATA
ARRAY DB 10 DUP(?)    ; 10 byte character array

.CODE
MOV AX , @DATA
MOV DS , AX
MOV SI , OFFSET ARRAY
MOV CL , 0

INSERT:
MOV AH , 01H  ; 08H IF WE DON'T WANT TO SHOW THE INSERTION
INT 21H

CMP AL , '$'
JE LINE

CMP AL , 'a'
JE STORE

CMP AL , 'e'
JE STORE

CMP AL , 'i'
JE STORE

CMP AL , 'o'
JE STORE

CMP AL , 'u'
JE STORE

CMP AL , 'A'
JE STORE

CMP AL , 'E'
JE STORE

CMP AL , 'I'
JE STORE

CMP AL , 'O'
JE STORE

CMP AL , 'U'
JE STORE
JMP INSERT

STORE:
MOV [SI] , AL
INC SI
INC CL
CMP CL , 10
JE IGNORE
JMP INSERT

IGNORE:
MOV AH , 01H    ; 08H IF WE DON'T WANT TO SHOW THE INSERTION
INT 21H
CMP AL , '$'
JE LINE
JMP IGNORE

LINE:
MOV SI , OFFSET ARRAY
MOV DL, 0AH
MOV AH , 02H
INT 21H           ; WE DON'T HAVE TO PRINT NEW LINE WHEN IT'S 08H
JMP PRINT

PRINT:
MOV AH , 02H
MOV DL , [SI]
INT 21H
MOV AH , 02H
MOV DL , 20H
INT 21H
INC SI
LOOP PRINT


.EXIT
END
