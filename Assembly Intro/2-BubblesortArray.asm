.MODEL SMALL

.DATA 
ARRAY DW 6DH,4H,2H,8H,3H,0H,9H,5H,3H,4H
      DW 7H,9H,8H,5H,2H,2H,1H,3H,0H,9H
	  DW 6H,5H,7H,6H,21H,9H,8H,5H,0H,4H
	  DW 0H,9H,5H,3H,4H,1H,3H,0H,7H,3H
	  DW 9H,8H,5H,2H,3H,4H,1H,3H,5H,4H                ; ALL BETWEEN 0H - 9H SO I CAN PRINT THE SORTED ARRAY EASILY
    
.CODE
MOV AX , @DATA
MOV DS , AX
MOV SI , OFFSET ARRAY
MOV CL , 49
MOV CH , 49

SORT:
MOV AX , [SI]
MOV BX , [SI+2]
CMP AX , BX
JA SWAP
INC SI
INC SI
DEC CL
CMP CL , 0
JE RELOOP 
JMP SORT

SWAP: 
MOV [SI] , BX  
MOV [SI+2] , AX
INC SI
INC SI
DEC CL
CMP CL , 0
JE RELOOP
JMP SORT

RELOOP:
MOV SI , OFFSET ARRAY
MOV CL , 49
DEC CH
CMP CH , 0
JE DONE
JMP SORT    
     
DONE:
MOV CL , 50

PRINT:
MOV AH , 02H
MOV DL , [SI]
ADD DL , 30H
INT 21H
INC SI 
INC SI
LOOP PRINT

.EXIT
END
